class Account {  

	
	private String name;  
	private int amount;  

	
	public String getName() {  
		return name;  
	}  
	
	public void setName(String name) {  
		this.name = name;  
	}  
	
	
	public float getAmount() {  
		return amount;  
	}  
	
	public void setAmount(int amount) {  
		this.amount = amount;  
	}  
  
}  



